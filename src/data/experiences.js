export const experiences = definitions.map((definition, index) => {
  const { loader, ...experience } = definition;
  const toy = createLazyToy(loader);

  return {
    ...experience,
    number: String(index + 1).padStart(2, '0'),
    Component: toy.Component,
    preload: toy.preload,
  };
});

export function getWrappedIndex(index) {
  const length = experiences.length;
  return (index + length) % length;
}

export function getRandomExperienceIndex(currentIndex) {
  if (experiences.length < 2) {
    return 0;
  }

  let nextIndex = currentIndex;

  while (nextIndex === currentIndex) {
    nextIndex = Math.floor(Math.random() * experiences.length);
  }

  return nextIndex;
}
